package Assignment6;

public class OneB {

    public static void main(String[] args) {
        System.out.println("Odd Numbers Series:");
        int odd = 1;
        while (odd <= 20) {
            System.out.print(odd + " ");
            odd += 2;
        }

        System.out.println("\nEven Numbers Series:");
        int even = 0;
        while (even <= 20) {
            System.out.print(even + " ");
            even += 2;
        }
    }
}
